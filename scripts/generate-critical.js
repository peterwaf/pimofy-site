/**
 * Critical-CSS generator.
 *
 * Renders representative marketing pages from a running dev server, extracts
 * the above-the-fold CSS with penthouse (at mobile + desktop widths), dedupes,
 * and writes it into views/partials/critical-css.ejs as an inline <style> block.
 *
 * Usage:
 *   1. Start the app locally (e.g. `npx vercel dev` or `npm run dev`).
 *   2. BASE_URL=http://localhost:3000 npm run generate:critical
 *
 * Re-run whenever styles.css or above-the-fold markup changes.
 */
const fs = require('fs');
const path = require('path');
const penthouse = require('penthouse');

const BASE_URL = process.env.BASE_URL || 'http://localhost:3000';
const CSS_PATH = path.join(__dirname, '..', 'styles.css');
const OUT_PATH = path.join(__dirname, '..', 'views', 'partials', 'critical-css.ejs');

// Representative pages. The marketing pages share the same header/nav/hero
// shell, so home + one interior page cover the above-the-fold surface.
const PAGES = ['/', '/solutions'];
const VIEWPORTS = [
  { width: 375, height: 812 }, // mobile
  { width: 1366, height: 900 }, // desktop
];

// Naive rule-level dedupe so concatenated penthouse outputs don't repeat.
function dedupeRules(css) {
  const seen = new Set();
  const out = [];
  // Split on the closing brace of top-level rules while keeping it attached.
  const chunks = css.match(/[^{}]+\{[^{}]*\}|@[^{]+\{(?:[^{}]*\{[^{}]*\})*[^{}]*\}/g) || [];
  for (const chunk of chunks) {
    const key = chunk.replace(/\s+/g, ' ').trim();
    if (key && !seen.has(key)) {
      seen.add(key);
      out.push(chunk.trim());
    }
  }
  return out.join('\n');
}

async function run() {
  const cssString = fs.readFileSync(CSS_PATH, 'utf8');
  let combined = '';

  for (const page of PAGES) {
    for (const vp of VIEWPORTS) {
      const url = `${BASE_URL}${page}`;
      process.stdout.write(`Extracting ${url} @ ${vp.width}x${vp.height}... `);
      const critical = await penthouse({
        url,
        cssString,
        width: vp.width,
        height: vp.height,
        timeout: 60000,
        pageLoadSkipTimeout: 8000,
        renderWaitTime: 200,
      });
      combined += `\n${critical}`;
      console.log(`${(critical.length / 1024).toFixed(1)}KB`);
    }
  }

  const deduped = dedupeRules(combined);
  const banner =
    '<%# AUTO-GENERATED by scripts/generate-critical.js — do not edit by hand. ' +
    'Regenerate with: BASE_URL=http://localhost:3000 npm run generate:critical %>\n';
  fs.writeFileSync(OUT_PATH, `${banner}<style>${deduped}</style>\n`);

  console.log(
    `\nWrote ${OUT_PATH} (${(deduped.length / 1024).toFixed(1)}KB critical CSS)`
  );
  process.exit(0);
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
